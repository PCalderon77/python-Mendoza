# Escribir un programa que pregunte al usuario su edad y muestre por pantalla todos los años que 
# ha cumplido (desde 1 hasta su edad).

# Por ejemplo si ingreso 4 años deberia mostrar algo asi:
# Has cumplido 1 años
# Has cumplido 2 años
# Has cumplido 3 años
# Has cumplido 4 años

edad=int(input("Ingrese su edad: "))
 
for i in range(1,edad+1,1):
    print(f"Has cumplido {i} años")
















